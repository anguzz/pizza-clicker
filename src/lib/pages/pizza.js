export function Onion() {    
    var can = document.getElementById("painter"),
        // @ts-ignore
        ctx = can.getContext('2d'),
        onion = new Image();
    onion.src = 'Onion.png';
    // @ts-ignore
    can.onclick= function(evt) {
        var x = evt.offsetX - onion.width/10,
            y = evt.offsetY - onion.height/10;
      ctx.drawImage(onion, x, y,70,70);};}
        
